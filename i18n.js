const LANG_FLAGS = {ru:'🇷🇺',en:'🇬🇧',zh:'🇨🇳',es:'🇪🇸',de:'🇩🇪'};
const LANG_NAMES = {ru:'RU',en:'EN',zh:'ZH',es:'ES',de:'DE'};

function detectLang() {
  const saved = localStorage.getItem('linkora_lang');
  if (saved && T[saved]) return saved;
  const browser = (navigator.language||'ru').slice(0,2).toLowerCase();
  return T[browser] ? browser : 'ru';
}

let currentLang = detectLang();

function t(key) {
  return (T[currentLang] && T[currentLang][key] !== undefined)
    ? T[currentLang][key]
    : (T.ru[key] || key);
}

function applyTranslations() {
  // Apply all translations by id - try both formats
  Object.keys(T.ru).forEach(key => {
    // Try key with dashes as id
    const id1 = 'i18n-' + key;
    const el1 = document.getElementById(id1);
    if (el1) { el1.innerHTML = t(key); return; }
    // Try key with underscores converted to dashes
    const id2 = 'i18n-' + key.replace(/_/g, '-');
    const el2 = document.getElementById(id2);
    if (el2) el2.innerHTML = t(key);
  });

  // Update lang button
  const btn = document.getElementById('langCurrentBtn');
  if (btn) btn.innerHTML = LANG_FLAGS[currentLang] + ' ' + LANG_NAMES[currentLang] + ' <span style="opacity:0.5;font-size:10px;">▾</span>';

  // Update active lang option
  document.querySelectorAll('.lang-option').forEach(el => {
    el.classList.toggle('active', el.dataset.lang === currentLang);
  });

  document.documentElement.lang = currentLang;
}

function setLang(lang) {
  if (!T[lang]) return;
  currentLang = lang;
  localStorage.setItem('linkora_lang', lang);
  applyTranslations();
  const menu = document.getElementById('langMenu');
  if (menu) menu.classList.remove('open');
}

function toggleLangMenu() {
  const menu = document.getElementById('langMenu');
  if (menu) menu.classList.toggle('open');
}

document.addEventListener('click', e => {
  const sw = document.getElementById('langSwitcher');
  if (sw && !sw.contains(e.target)) {
    const menu = document.getElementById('langMenu');
    if (menu) menu.classList.remove('open');
  }
});

document.addEventListener('DOMContentLoaded', applyTranslations);
